print("Definindo uma lista")
myFruitList = ["apple", "banana", "cherry"]
print(myFruitList)
print(type(myFruitList))

print("lista por posição")
print(myFruitList[0])
print(myFruitList[1])
print(myFruitList[2])

print("Alterando os valores em uma lista")
myFruitList[2] = "orange"
print(myFruitList)

print("Apresentando o tipo de dados tupla")
myFinalAnswerTuple = ("apple", "banana", "pineapple")
print(myFinalAnswerTuple)
print(type(myFinalAnswerTuple))

print("Acessando uma tupla por posição")
print(myFinalAnswerTuple[0])
print(myFinalAnswerTuple[1])
print(myFinalAnswerTuple[2])

print("Apresentando o tipo de dados do dicionário")
myFavoriteFruitDictionary = {
  "Akua" : "apple",
  "Saanvi" : "banana",
  "Paulo" : "pineapple"
}
print(myFavoriteFruitDictionary)
print(type(myFavoriteFruitDictionary))

print("Acessando um dicionário pelo nome")
print(myFavoriteFruitDictionary["Akua"])
print(myFavoriteFruitDictionary["Saanvi"])
print(myFavoriteFruitDictionary["Paulo"])